"""
FitLife - MVP версия 1.2.

Модуль для расчета индекса массы тела (ИМТ) и рекомендованной нормы воды
на основе введенных пользователем данных.

Запрашивает у пользователя:
- Имя
- Возраст (полных лет)
- Вес (в кг)
- Рост (в метрах)

Выводит:
- Отчет с ИМТ (с одним знаком после запятой)
- Рекомендацию по потреблению воды (в литрах)
"""


# Константы
WATER_PER_KG_ML = 30    # Рекомендация по воде: 30 мл на 1 кг веса
ML_PER_LITER = 1000     # 1 литр = 1000 миллилитров
HEIGHT_PROMPT = 'Введите свой рост (в метрах, например 1.75): '

# 1. Знакомство
print('Приветствую в приложение FitLife!')
user_name = input('Как вас называть? ').title()

# 1.1 Ввод возраста с защитой
while True:
    try:
        user_age = int(input('Введите полных лет: '))
        break
    except ValueError:
        print('Ошибка: возраст должен быть целым числом. Попробуйте снова.')


# 2. Сбор данных

user_weight = float(input('Введите свой вес: ').replace(',', '.'))
user_height = float(input(HEIGHT_PROMPT).replace(',', '.'))


# 3. Логика расчетов
# Формула ИМТ: вес разделить на (рост в квадрате)
bmi = user_weight / (user_height ** 2)


water_ml = user_weight * WATER_PER_KG_ML    # Подсчет воды: вес * 30 мл
water_l = water_ml / ML_PER_LITER           # Переводим миллилитры в литры

# 4. Вывод красивого результата
print(f'Отчет для пользователя: {user_name} ({user_age} г.)')
print(f'Твой Индекс Массы Тела: {bmi:.1f}')
print(f'Рекомендуемая норма воды: {water_l:.1f} л. в день')
print('\nРасчет окончен. Будьте здоровы!')
